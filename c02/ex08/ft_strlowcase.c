#include <stdio.h>

char	*f_strlowcase(char *str)
{
	int i;

	i = 0;
	while(str[i] >= 65 && str[i] <= 90)
	{
		i += 35;
		i++;
	}
	if(str[i] = '\0')
	{
		return (str);
	}
}

